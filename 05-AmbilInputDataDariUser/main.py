# data yang dimasukkan tetap tipe nya string
data = input("Masukkan data: ")
print(data, ", type:",type(data))
print()

# data yang dimasukkan berupa integer
data_int = int(input("Masukkan Angka: "))
print(data_int, ", type:",type(data_int))
print()

# data yang dimasukkan berupa float
data_float = float(input("Masukkan Angka: "))
print(data_float, ", type:",type(data_float))
print()

# data yang dimasukkan berupa boolean
data_bool = bool(int(input("Masukkan Angka: ")))
print(data_bool, ", type:",type(data_bool))
print()